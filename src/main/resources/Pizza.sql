DROP DATABASE IF EXISTS Pizza;
CREATE DATABASE Pizza;
USE Pizza;

CREATE TABLE PIZZA(
    PIZZAID Integer NOT NULL,
    PIZZANAME varchar(20),
    PRICE double,
    CONSTRAINT XYZ_PIZZA_PK PRIMARY KEY(PIZZAID)
);

CREATE TABLE PIZZA_ORDER(
    ORDERID Integer NOT NULL AUTO_INCREMENT,
    CUSTOMERNAME varchar(30) NOT NULL,
    CONTACTNUMBER varchar(10) NOT NULL,
    PIZZAID Integer,
    NUMBEROFPEICESORDERED Integer,
    BILL double,
    CONSTRAINT PIZZA_ORDER_PK PRIMARY KEY(ORDERID),
    CONSTRAINT PIZZA_ORDER_FK FOREIGN KEY(PIZZAID) REFERENCES PIZZA(PIZZAID)
);

